num1 = 15   #The first number goes here.
num2 = 7    #The second number goes here.

#Calculations stored in variables.
add = num1 + num2
sub = num1 - num2
mul = num1 * num2
expo = num1 ** num2
div = num1 / num2
mod = num1 % num2
floor = num1 // num2

#Printing answers.
print(num1, "+", num2, "=", add)
print(num1, "-", num2, "=", sub)
print(num1, "x", num2, "=", mul)
print(num1, "/", num2, "=", div)
print(num1, "^", num2, "=", expo)
print(num1, "%", num2, "=", mod)
print(num1, "//", num2, "=", floor)